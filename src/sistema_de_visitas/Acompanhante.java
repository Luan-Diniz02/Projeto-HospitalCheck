package sistema_de_visitas;

/**
 *
 * @author Luan Diniz
 */
public class Acompanhante {
    private String nome, cpf, sexo, categoria;
    private int idade;

    public Acompanhante(String nome, String cpf, String sexo, String categoria, int idade) {
        this.nome = nome;
        this.cpf = cpf;
        this.sexo = sexo;
        this.categoria = categoria;
        this.idade = idade;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public String getCategoria() {
        return categoria;
    }

    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }
    
    
}
